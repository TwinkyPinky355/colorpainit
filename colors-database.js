// Функция для получения цвета по каталогу и коду (без учета регистра)
function getColorFromCatalog(catalog, code) {
    const catalogKey = catalog.toLowerCase();
    const codeKey = code.toString().toUpperCase();
    
    if (colorDatabase[catalogKey] && colorDatabase[catalogKey][codeKey]) {
        return colorDatabase[catalogKey][codeKey];
    }
    return null;
}

// Функция для получения всех кодов из каталога
function getAllCodesFromCatalog(catalog) {
    const catalogKey = catalog.toLowerCase();
    if (colorDatabase[catalogKey]) {
        return Object.keys(colorDatabase[catalogKey]);
    }
    return [];
}